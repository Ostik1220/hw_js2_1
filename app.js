const photoItems = galleryItems.map((photo) => {
  return `<li class="gallery__item">
    <a
      class="gallery__link"
      href="${photo.original}"
    >
      <img
        class="gallery__image"
        src="${photo.preview}"
        data-source="${photo.original}"
        alt="${photo.description}"
      />
    </a>
  </li>`;
});

const photoDom = photoItems.join("");
const gallery = document.querySelector(".js-gallery");
gallery.innerHTML = photoDom;

const links = document.querySelectorAll(".gallery__link");
const lightbox = document.querySelector(".js-lightbox");
const lightboxImage = document.querySelector(".lightbox__image");
const closeButton = document.querySelector(".lightbox__button");

links.forEach((link) => {
  link.addEventListener("click", (e) => {
    e.preventDefault();
    lightbox.style.opacity = "1";
    lightbox.style.pointerEvents = "visible";
    lightboxImage.setAttribute("src", e.target.dataset.source);
  });
});

closeButton.addEventListener("click", (e) => {
  e.preventDefault();
  lightbox.style.opacity = "0";
  lightbox.style.pointerEvents = "none";
  lightboxImage.setAttribute("src", "");
});
